# arithematic operators
# addition, subtraction, mulitplication and division
number = 1 + 2 * 3 / 4.0
print(number)

# modulo operator
remainder = 11 % 4
print(remainder)

# power operator
squared = 7 ** 2
cubed = 4 ** 3
print(squared)
print(cubed)

# operators with strings
helloworld = 'hello' + ' ' + 'world'
print(helloworld)

# multiplying strings
lotsofhellos = '--hello--' * 10
print(lotsofhellos)

# operators with lists
even_numbers = [2,4,6,8]
odd_numbers = [1,3,5,7]
all_numbers = even_numbers + odd_numbers
print(all_numbers)

# multiplying lists
print([1,2,3] * 3)
